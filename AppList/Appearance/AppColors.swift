import Foundation
import UIKit

extension UIColor {
    
    class func lightBlueColor() -> UIColor {
        return UIColor(red: 20.0/255.0, green: 169.0/255.0, blue: 225.0/255.0, alpha: 1.0)
    }
    
    class func lightBlackColor() -> UIColor {
        return UIColor(red: 52.0/255.0, green: 52.0/255.0, blue: 52.0/255.0, alpha: 1.0)
    }
}